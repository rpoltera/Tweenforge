pct exec "${CTID}" -- bash -c "cd ${APP_PATH} && tar xzf app.tar.gz && rm app.tar.gz && mkdir -p voices renders"

echo ">> Building dashboard..."
pct exec "${CTID}" -- bash -c "cd ${APP_PATH} && npm install --no-fund --no-audit && npm run build && npm install -g serve >/dev/null"

cat > /tmp/tweenforge.service << 'UNIT'
[Unit]
Description=Tweenforge Dashboard
After=network.target
[Service]
WorkingDirectory=/opt/tweenforge
ExecStart=/usr/bin/env serve -s dist -l 5173
Restart=always
[Install]
WantedBy=multi-user.target
UNIT
cat > /tmp/tweenforge-render.service << 'UNIT2'
[Unit]
Description=Tweenforge Render Engine
After=network.target
[Service]
WorkingDirectory=/opt/tweenforge/render
Environment=PIPER_VOICES_DIR=/opt/tweenforge/voices
ExecStart=/usr/bin/env uvicorn server:app --host 0.0.0.0 --port 8080
Restart=always
[Install]
WantedBy=multi-user.target
UNIT2
