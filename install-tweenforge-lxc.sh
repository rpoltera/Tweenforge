#!/usr/bin/env bash
#
# install-tweenforge-lxc.sh  —  SELF-CONTAINED installer (app + render engine embedded).
# Run ON THE PROXMOX HOST as root. Creates a Debian LXC, installs Node + Python +
# ffmpeg, unpacks the bundled Tweenforge dashboard AND render engine, builds the
# dashboard, and starts both as systemd services. No repo, no uploads needed.
#
#   Dashboard      :  http://<container-ip>:5173
#   Render service :  http://<container-ip>:8080   (set this as the Render URL in Settings)
#
set -euo pipefail

HOSTNAME="tweenforge"
CORES=2; MEMORY_MB=3072; SWAP_MB=512; DISK_GB=10
PORT=5173; RENDER_PORT=8080
APP_PATH="/opt/tweenforge"

command -v pct >/dev/null || { echo "ERROR: pct not found. Run this on the Proxmox host."; exit 1; }

STORAGE=$(pvesm status -content rootdir 2>/dev/null | awk 'NR>1{print $1; exit}')
[ -n "${STORAGE:-}" ] || { echo "ERROR: no storage supports container disks (content 'rootdir')."; exit 1; }
echo ">> Storage: ${STORAGE}"
TMPL_STORE=$(pvesm status -content vztmpl 2>/dev/null | awk 'NR>1{print $1; exit}'); TMPL_STORE="${TMPL_STORE:-local}"
TEMPLATE=$(pveam list "${TMPL_STORE}" 2>/dev/null | awk '/debian-1[0-9]-standard/{print $1; exit}')
if [ -z "${TEMPLATE:-}" ]; then
  echo ">> Downloading Debian template..."; pveam update >/dev/null 2>&1 || true
  TMPL_NAME=$(pveam available --section system 2>/dev/null | awk '/debian-1[0-9]-standard/{print $2}' | sort -V | tail -1)
  [ -n "${TMPL_NAME:-}" ] || { echo "ERROR: no Debian template available."; exit 1; }
  pveam download "${TMPL_STORE}" "${TMPL_NAME}"; TEMPLATE="${TMPL_STORE}:vztmpl/${TMPL_NAME}"
fi
echo ">> Template: ${TEMPLATE}"
BRIDGE=$(ls /sys/class/net 2>/dev/null | grep -m1 '^vmbr' || echo vmbr0); echo ">> Bridge: ${BRIDGE}"
CTID=$(pvesh get /cluster/nextid); echo ">> Container ID: ${CTID}"

pct create "${CTID}" "${TEMPLATE}" --hostname "${HOSTNAME}" \
  --cores "${CORES}" --memory "${MEMORY_MB}" --swap "${SWAP_MB}" \
  --rootfs "${STORAGE}:${DISK_GB}" --net0 "name=eth0,bridge=${BRIDGE},ip=dhcp" \
  --unprivileged 1 --features nesting=1 --onboot 1
pct start "${CTID}"

echo ">> Waiting for network..."
for i in $(seq 1 30); do pct exec "${CTID}" -- getent hosts deb.nodesource.com >/dev/null 2>&1 && break; sleep 2; done

echo ">> Installing Node.js 22, Python, ffmpeg..."
pct exec "${CTID}" -- bash -c "apt-get update -qq && apt-get install -y -qq curl ca-certificates tar ffmpeg python3 python3-pip fonts-dejavu-core >/dev/null"
pct exec "${CTID}" -- bash -c "curl -fsSL https://deb.nodesource.com/setup_22.x | bash - >/dev/null"
pct exec "${CTID}" -- bash -c "apt-get install -y -qq nodejs >/dev/null"
pct exec "${CTID}" -- bash -c "pip3 install --break-system-packages -q fastapi uvicorn pillow"

echo ">> Unpacking bundled app + render engine..."
